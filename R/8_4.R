#########散布圖(scatter chart)#################
肱骨 <- c(38,56,59,64,74)
股骨 <- c(41,63,60, 72,84)
plot( # plot繪圖函式，預設為散布圖，函式說明請參閱附錄A
  x=肱骨, # x 軸各值，軸標籤同變數名
  y=股骨  # y 軸各值，軸標籤同變數名
)
x <- cor( # 資料組相關性(correlation )分析，函式說明請參閱附錄A
  x=肱骨,y=股骨,
  method ='pearson'  # 以皮爾森法
) 
print(x)  # 印出本例相關係數之結果
