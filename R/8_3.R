    其他=c("天氣","飛航延遲"),
    設備=c("班機延遲到達", "機械故障"),  
    人員=c("乘客登機程序", "機艙清潔延遲", "駕駛人員不足", "機員遲到"),
    原料=c("行李延遲送達", "燃料延遲", "餐飲服務延遲"),
    方法=c("起飛公布系統不良", "延遲報到的程序", "等候延遲的乘客")),
  effect="班機起飛延遲" # 影響之名稱(文字)
)
