A <- matrix( # 叫用內建函式建構矩陣物件
  data=c(320, 280,460,280, # 構成資料為一組vector型態之物件
         480,360,580,0,
         540,420,200,880
  ), 
  nrow = 3,  # 資料分列數
  byrow=TRUE # 資料元素是否依列順序填入
)
B <- matrix(
  data=c(210,180,330,180,  # 同上A
         400,300,450,40,
         420,280,180,740
  ), 
  nrow = 3,  # 同上A
  byrow=TRUE # 同上A
)
print(A) # (1)將A矩陣物件印出
print(B) # (1)將B矩陣物件印出
print(A + B) # (2)將A、B兩矩陣相加之結果印出
