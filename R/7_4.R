T <-matrix(   # 建構遞移矩陣物件，函式說明請參閱附錄 a
  c(0.6,0.3,0.1,    # 3*3 矩陣資料(向量物件)
    0.4,0.3,0.3,
    0.3,0.3,0.4),
  ncol=3, 
  byrow = FALSE
)
print(T)  # a .印出遞移矩陣
X0 <- matrix(   # 建構初始人口分布狀態
  c(0.80,0.15,0.05),
  nrow =3,
  byrow =TRUE
)
X1 <- T %*% X0
print(X1)  # b. 印出第一回的分布
X2 <- T %*% X1
print(X2)  # c. 印出第二回的分布
