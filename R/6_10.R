#####使用R 基本運算元(operator)計算#####
R<- 100  #  每期金額
i<- 0.0025  #  每期利率
n <- 24  #  期數
Numerator <- 1-(1+ i)^(-n)
ca.factor <- Numerator/i # ca.factor <- [(1+ i)^ (n -1)]/i
P <-R * ca.factor
print(P)  # 印出本例結果

##########運用sum函式對vector便捷的處理#########
R<- 100  #  每期金額
i<- 0.0025  #  每期利率
n <- 24  #  期數
S <- sum(  # 加總向量元素，函式說明請參閱附錄A
  100*(1+i)^(0:(n-1)) # 將0 至 n-1 期的複利加總 
) 
print(S)  # 印出年金複利加總
P<-S/((1+0.0025)^n)  # 以 S 求算複利下之現值
print(P)  # 印出本例結果
